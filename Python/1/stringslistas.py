string = "You challenge makoa!?"

print(string) #Imprimir como cadena normal

print(string[0]) #Tratamos a string como un arreglo i.e. arr[0] 

print(string[-1]) #Imprime la posicion n-1, (cuando hay numeros negativos)

print(string[0],string[1], string[2]) #imprimir los caracteres (separados por un espacio = , ) de 0 1 2 

print(string[0:10]) #Tomar la subcadena de i = 1 hasta n-1 toda junta
                    #También sirve para numeros negativos, pero sería al reves

print(string[0:18:3]) #hacer saltos progresivos en la cadena de 3 en 3. para toda la cadena sería

#print(string[0:string.long: #saltos])

print(string[::-1]) #para imprimir la reversa de una palabra. 
