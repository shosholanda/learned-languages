#Los numeros (constantes) nunca cambian. 
numero_uno = 3
numero_dos = 3

suma = numero_uno + numero_dos

#Para imprimir varias cosas se hace una separacion con coma de los args. 
print("Suma", suma)

resta = numero_uno - numero_dos
print("Resta", resta)

multiplicacion = numero_uno * numero_dos
print("Multiplicacion", multiplicacion)

division = numero_uno / numero_dos
print("Divis√≥n", division) #siempre regresa un flotante 

division_entera = numero_uno // numero_dos
print("Division entera", division_entera)

exponencial = numero_uno ** numero_dos #Numero1 elevado a numero_dos
print("Exponencial", exponencial)
